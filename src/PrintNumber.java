public class PrintNumber {
    public static void main(String[] args) {
        //this for loop starts at 5 and adds by 1 each loop until it is at 19
        //in each loop it will print what the value of i is
        for (int i = 5; i <= 19; i = i + 1) {
            //prints value of i
            System.out.println(i);
        }
    }
}
